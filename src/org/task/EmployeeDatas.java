package org.task;

public class EmployeeDatas {
	
	private String name;
	
	private int id;
	
	private long phno;
	
	public String getName(){
		return name;		
	}

	public void setName(String name){
		
		this.name = name;
		
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public long getPhno() {
		return phno;
	}

	public void setPhno(long phno) {
		this.phno = phno;
	}
	
	
	
	

}
